package sept.ex_27092024.Tasks_27092024;

import java.util.Scanner;

public class Task1_27092024 {
    public static void main(String[] args) {
        //while loop
        //print even Numbers from 1 to n
        int i=1;

        while (i<=10){

                System.out.println(i);
                i++;
        }

        //Do while Loop

        int j=12;
        do {

                System.out.println(j);
                j++;

        }while (j<=10);

    }
}
