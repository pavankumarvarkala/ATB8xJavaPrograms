package sept.ex_16092024;

public class Lab031_CompoundAssignmentOperator {
    public static void main(String[] args) {
        //Compound Assignment operators
        int a=10;
        System.out.println(a);
        // +=, -=, *=, /=
        a +=20; //a=a+20;
        System.out.println(a);

        a -=20; //a=a-20;
        System.out.println(a);

        a *=20; //a=a*20;
        System.out.println(a);

        a /=20; //a=a/20;
        System.out.println(a);
    }
}
