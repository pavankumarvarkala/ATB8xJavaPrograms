package sept.ex_18092024;

public class Lab045 {
    public static void main(String[] args) {
        //post increment
        //Value is Stored first and then incremented.

        int a=20;
        int b=a++;
        System.out.println(a);
        System.out.println(b);

        int c=10;
        System.out.println(c++);
        System.out.println(c);
    }
}
